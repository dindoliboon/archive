.data
.align 2

# =========================================================================

printf: .asciiz "Hello, my name is Mr. G\n"

# =========================================================================

.text
.align 2

main:

# =========================================================================
# :::::	OP ::::	ARGUMENTS :::::::::::::	COMMENTS ::::::::::::::::::::::::::
# =========================================================================

	la	$a0, printf
	li	$v0, 4
	syscall				# print "Hello ... "

# =========================================================================

	li	$v0, 10
	syscall				# exit program

